                                <p>So, what is Quizbowl, exactly? We frequently compare ourselves to the <i>Jeopardy!</i> TV show, but there are two major differences: Quizbowl questions have a more academic focus and Quizbowl is played on a team. Game play is also somewhat different in Quizbowl than it is on any TV game show. A moderator reads a question called a <b>tossup</b> to two teams of four people. Each player has a push-button buzzer and can buzz in at any point during the reading of the tossup to win 10 points for their team. In certain Quizbowl formats, you can get 15 points for a tossup if you answer it especially early. This is called a <b>power</b>. However, you can also get penalized five points for answering early and getting the question wrong (this is called <b>negging</b>). If one team answers the tossup correctly, the moderator reads a <b>bonus</b> to that team. Bonuses come in groups of three questions each, with each one worth an additional 10 points. Each part of the bonus relates to the othersâ€”for example, a bonus might ask you to identify various works by the same author or figures from a Greek myth.</p>
