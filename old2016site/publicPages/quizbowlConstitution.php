                            <div class="panel-body">
                                <p><b>ARTICLE I: CLUB DEFINITION</b></p>
                                <p><b>Section 1: Club Definition</b></p>
                                <p>This Constitution shall govern the actions and policies of the Thomas Jefferson High School for Science and Technology (TJ) Quizbowl Club, hereinafter known as ‘the Club’, which shall be affiliated with the Virginia High School League (VHSL) Scholastic Bowl.</p>
                                <p><b>Section 2: Club Activities</b></p>
                                <p>The Club shall further the academic aspirations of the TJ student body through a variety of academic competitions, including It's Academic television tapings, the VHSL Scholastic Bowl, regular Quizbowl tournaments hosted by high schools and colleges, and national Quizbowl tournaments such as the High School National Championship Tournament (HSNCT) and the National Scholastic Championship (NSC).</p>
                                <p>When possible, the Club shall host tournaments at TJ to provide other schools with the opportunity for competition, to raise funds, and to provide Members with experience officiating matches and coordinating tournament logistics.</p>
                                <p>All Members and Officers shall abide by the TJ Honor Code, the TJ Extracurricular Participation Policy, and the Fairfax County Public Schools (FCPS) Student Rights and Responsibilities. Conduct by Officers or Members deemed to violate these rules shall be addressed by the Club Sponsor(s) and TJ administration, who shall determine the appropriate disciplinary action.</p>
                                <p>All Members and Officers shall conduct themselves in an exemplary manner. Club activities shall be conducted in a transparent and honest manner. No Member shall face unfair discrimination related to any Club activities.</p>
                                <p><b>Section 3: Club Meetings</b></p>
                                <p>The Club shall hold general meetings twice weekly during eighth period in order to practice for competitions. Members shall sign up to attend eighth period practices using the TJ Intranet system. Practices shall consist of reading questions, giving short lectures, or any other activity deemed necessary by the Members. Officers shall organize any additional practices during lunch or outside of school hours.</p>
                                <p><b>Section 4: Tournaments</b></p>
                                <p>Regular tournament information shall be announced to Members by email, in person during eighth periods, or through other means of mass communication. Any Member may sign up for a regular tournament on a first-come basis before a predetermined date, to be announced no later than ten days prior to the tournament. Any Member who fails to express interest before this date may not be permitted to attend the tournament. Any Member who, without good cause, is absent from the tournament or retracts their attendance later than three days prior to the tournament may be suspended from attending future tournaments.</p>
                                <p>National tournament information shall be announced to Members in the same manner. Officers shall determine team rosters for national tournaments on the basis of criteria which shall include past nationals experience and regular tournament performance. If interest in attending nationals exceeds the number of spots available, Officers shall hold tryouts to determine the rosters.</p>
                                <p>It’s Academic and VHSL Scholastic Bowl information shall be announced to Members in the same manner. Officers shall determine team rosters for these competitions.</p>
                                <p><b>ARTICLE II: OFFICER POSITIONS</b></p>
                                <p><b>Section 1: Officers</b></p>
                                <p>All administrative power shall be vested in the Officers of the Club, which shall consist of the two Co-Captains, the Treasurer, the two Tournament Directors, the Public Relations Officer, the Secretary, and the Webmaster.</p>
                                <p>Officers shall serve for a term of one academic year.</p>
                                <p><b>Section 2: Co-Captains</b></p>
                                <p>There shall be two Co-Captains of equal rank, who shall direct all Club activities. The Co-Captains shall oversee practices, determine the tournament schedule, register for tournaments, supervise the creation of team rosters, ensure proper preparation for Club events, attend such events when able, and oversee the conduct of other Members in attendance. The Co-Captains shall communicate with the Club Sponsor(s) about all Club decisions and delegate any tasks deemed necessary to other Officers and Members.</p>
                                <p>The Co-Captains shall serve as the official liaisons to the TJ administration, other TJ organizations, other schools, and any other organization as Club activities shall compel.</p>
                                <p><b>Section 3: Treasurer</b></p>
                                <p>There shall be one Treasurer, who shall oversee Club finances, seek funding from Academic Boosters and other organizations, work with the Club Sponsor(s) to obtain approval for Club transactions, and manage funds raised from Club activities.</p>
                                <p><b>Section 4: Tournament Directors</b></p>
                                <p>There shall be two Tournament Directors, who shall oversee tournaments hosted by the Club. The Tournament Directors shall communicate with registered teams, ensure receipt of fees, and manage all tournament logistics, including the question set, food, room setup, buzzers, prizes, staffing, statistics, and the storage and security of tournament materials.</p>
                                <p><b>Section 5: Public Relations Officer</b></p>
                                <p>There shall be one Public Relations Officer, who shall manage external Club communications. The Public Relations Officer shall publicize Club events, promote the Club in the broader TJ and Quizbowl community, encourage Members’ contributions to Academic Boosters, and attend activities fairs for the purpose of recruiting new members from the TJ student body.</p>
                                <p><b>Section 6: Secretary</b></p>
                                <p>There shall be one Secretary, who shall manage internal Club communications. The Secretary shall announce tournaments, Club events, and any other information deemed relevant by the Officers. The Secretary shall maintain the email list or other means of mass communication and track sign-ups for tournaments and other Club events.</p>
                                <p><b>Section 7: Webmaster</b></p>
                                <p>There shall be one Webmaster, who shall maintain and improve the Club Website.</p>
                                <p><b>ARTICLE III: ELECTIONS</b></p>
                                <p><b>Section 1: Electing Officers</b></p>
                                <p>General Elections shall take place no earlier than HSNCT and no later than the last eighth period meeting of the current academic year. Officers shall be elected by the concurrence of a plurality of Voting Members.</p>
                                <p>If an Officer position should become vacant before 1 May of the current academic year, it shall be filled by Special Election to be held within two calendar weeks. Special Elections shall proceed according to Section 4 of this Article. Voting and candidacy status shall be determined according to Sections 2 and 3 of this Article.</p>
                                <p><b>Section 2. Voting Status</b></p>
                                <p>Any Member may vote who has attended one outside tournament or staffed a Club-hosted tournament in the current academic year. Voting status shall be valid until the next General Election.</p>
                                <p><b>Section 3. Candidacy Status</b></p>
                                <p>Any Member may be an Officer who has attended either two regular tournaments or one national tournament and staffed a Club-hosted tournament in the current academic year. Candidacy status shall be valid until the next General Election.</p>
                                <p><b>Section 4. Election Procedures</b></p>
                                <p>The Time, Places, and Manner of holding Elections shall be determined by a Senior Co-Captain, or another Senior Officer, or otherwise by a Co-Captain if there should be no Senior Officers, and communicated to Members. The Officer shall verify voting and candidacy status and count ballots.</p>
                                <p>Election results shall be certified by the Officers and the Club Sponsor(s). Vote totals shall be disclosed to any Member upon request.</p>
                                <p><b>Section 5. Assumption of Officer Positions</b></p>
                                <p>Newly elected Officers shall assume their positions upon certification of election results.</p>
                                <p><b>Section 6. Dual Officer Positions</b></p>
                                <p>No Member shall hold or be a candidate for more than one Officer position in the same academic year.</p>
                                <p><b>Section 7. Officer Meetings</b></p>
                                <p>Except in the case of significant conflicting activities, all Officers shall convene in periodic meetings, arranged either among themselves or by request of the Club Sponsor(s).</p>
                                <p><b>Section 8. Impeachment</b></p>
                                <p>Any Voting Member may move the impeachment of an Officer by submitting a written grievance to the Officers. A motion to impeach shall not be sustained without the concurrence of three Officers. The Officer whose removal is sought shall be able to submit a written response.</p>
                                <p>Impeachment shall be followed by a discussion period of two calendar weeks. At the conclusion of this period, removal shall be enacted by the concurrence of two-thirds of Officers, excluding the Officer whose removal is sought, and the subsequent concurrence of two-thirds of Voting Members, with at least one-half of all ballots cast.</p>
                                <p><b>ARTICLE IV: AMMENDMENT PROCESS</b></p>
                                <p><b>Section 1: Ammendment Process</b></p>
                                <p>Any Voting Member may propose Amendments to this Constitution. Amendments shall be sponsored by one Officer and ratified by two-thirds of Voting Members. Ratification shall proceed according to Section 4 of Article III.</p>
                                <p><b>ARTICLE V: TRANSPARENCY</b></p>
                                <p><b>Section 1: Availability</b></p>
                                <p>This Constitution shall be made available on the Club Website.</p>
                                <p><b>ARTICLE VI: Ratification</b></p>
                                <p><b>Section 1: Voting Status</b></p>
                                <p>Any Member may vote in the Ratification of this Constitution who has attended one outside tournament or staffed a Club-hosted tournament in the current academic year.</p>
                                <p><b>Section 2: Ratification Process</b></p>
                                <p>This Constitution shall be established by the Ratification of a majority of Voting Members, as defined by Section 1 of this Article. Ratification shall proceed according to Section 4 of Article III.</p>
                                
