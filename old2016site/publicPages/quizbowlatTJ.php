                                <p><b>Tournaments</b> are of varying difficulty: novice, regular, and national. TJ often sends multiple teams to a tournament, designated A, B, C, etc. They’ll almost always be within driving distance of TJ - campuses in Maryland or Virginia - although we do sometimes venture farther.</p>
                                <p><b>Novice tournaments</b> are designed for new high school players, and are restricted to those who have not attended a national tournament. They typically have easier questions and opponents.</p>
                                <p><b>Regular-season tournaments</b> are open to everyone, from novices to the most experienced. We don’t make cuts for these tournaments - register in time and you will play. </p>
                                <p><b>National tournaments</b>… the most prestigious tournaments in high school Quizbowl. They last an entire weekend and often require long-distance travel. Our team attends two national tournaments: NAQT’s High School National Championship Tournament and PACE’s National Scholastic Championship. The fee for national tournaments will be calculated by officers, and depend on club finances, travel fees, and registration fees.</p>
                                <p>To select teams <i>as fairly as possible</i>, officers consider the following: past performance at tournaments (with harder tournaments carrying more weight), past nationals experience, staffing at our two tournaments (expounded below), and practice attendance. Tryouts may take place for national tournaments.</p>
